public class 面向对象三大特性 {
    /**
     * 说到面向对象就不得不提面向对象的特性,通常来说有三大特性:封装,继承,多态\
     *
     * 1.封装:封装将事物的`行为`和`数据`包裹为`方法`和`属性`,将部分实现细节隐藏起来,对外只提供访问的方式.
     * 封装最主要的功能在于我们能修改自己的实现代码，而不用修改那些调用我们代码的程序片段.
     * 适当的封装可以让程式码更容易理解与维护，也加强了程式码的安全性.
     * 最常见的体现就是JavaBean类
     *
     * 2.继承:继承就是想有共性的方法和属性的对象抽取出来并封装成一个父类,这样就子类继承父类的特征和行为,使得子类对象（实例）具有父类的实例域和方法.
     * 继承可以理解成is-a 类似于物种分类,猫属于猫科,狗属于犬科,他们都属于哺乳动物.拥有继承之后,猫狗继承哺乳动物,那么就有了哺乳动物的属性和行为.
     * 继承可以提高代码的复用性,但是耦合性也增加了
     * 注意:Java可以多层继承,但是不可以一次继承多个(除了接口)
     *
     * 3.多态就是多种形态，同一个行为具有多个不同表现形式或形态的能力。
     * 多态存在的三个必要条件:
     * 1.继承
     * 2.重写
     * 3.父类引用指向子类对象<<<
     *
     * 有的情况还会提到抽象,抽象见抽象包的描述
     */
}
